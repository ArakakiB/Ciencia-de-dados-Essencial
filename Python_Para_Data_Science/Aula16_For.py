'''
O for percorre uma lista item a item. Nesse caso, vemos que letra é um item e
o my_string é uma lista
É Possivel tambem usar o ELSE com for
'''

my_string = 'Python'

# Para cada 'letra' em 'my_string':
for letra in my_string:
    print(letra)

'''
Por padrao a Função range já vem por padrao assim: (start = 0, stop, step = 1)
'''
for n in range(0, 3, 1):
    print(n)

for n in range(3):
    print(n)




