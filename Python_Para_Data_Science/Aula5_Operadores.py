'''
+ , SOMA
- , Subtração
* , Multiplicação
/ , Divisão
// , Divisão, porem, somentem o numero arredondado para inteiro
** , Exponencial
% , Retorna o resto de uma divisão
'''

print(20 + 20)
print(40 - 50)
print(2 * 8)
print(40 / 2)
print(10 / 3)
print(10 // 3)
print(2 ** 4)
print(10 % 3)

# Operadores com Strings
print('Bruno' + ' ' + 'Arakaki')
# Mas ainda é melhor fazer da maneira de baixo
print('Bruno', 'Arakaki')

print('10' + '10')
print(20 * 'A')  # Multiplica 20x a letra A